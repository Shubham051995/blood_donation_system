							Blood Tips<br><br>
							Beat the myth<br>
							Donating blood is safe and simple. It takes approximately 10-15 minutes to complete the blood donation process. Any healthy adult between 18 years and 60 years of age can donate blood. This is what you can expect when you are ready to donate blood:<br><br>
							You walk into a reputed and safe blood donation centre or a mobile camp organized by a reputed institution.<br>
							A few questions will be asked to determine your health status (general questions on health, donation history etc). Usually you will be asked to fill out a short form.<br>
							Then a quick physical check will be done to check temperature, blood pressure, pulse and hemoglobin content in blood to ensure you are a healthy donor.<br>
							If found fit to donate, then you will be asked to lie down on a resting chair or a bed. Your arm will be thoroughly cleaned. Then using sterile equipments blood will be collected in a special plastic bag. Approximately 350 ml of blood will be collected in one donation. Those who weigh more than 60 Kg can donate 450 ml of blood.<br>
							Then you must rest and relax for a few minutes with a light snack and something refreshing to drink. Some snacks and juice will be provided.<br>
							Blood will be separated into components within eight hours of donation<br>
							The blood will then be taken to the laboratory for testing.<br>
							Once found safe, it will be kept in special storage and released when required.<br>
							The blood is now ready to be taken to the hospital, to save lives.<br><br><br>
							Blood Groups<br>
							Blood type is determined by which antibodies and antigens the person's blood produces. An individual has two types of blood groups namely ABO-grouping and Rh-grouping. Rh is called as the Rhesus factor that has come to us from Rhesus monkeys.<br><br>
							Most humans are in the ABO blood group. The ABO group has four categories namely <br>
							1) A group 2) B group 3) O group and 4) AB group<br>
							In the Rh- group, either the individual is said to be Rh- Negative or Rh- Positive.<br><br>
							Thus blood group of any human being will mainly fall in any one of the following groups. <br>
							A positive or A negative <br>
							B positive or B negative <br>
							O positive or O negative <br>
							AB positive or AB negative.<br><br><br>
							Universal Donors and Recipients<br>
							The most common blood type is O, followed by type A.<br><br>
							Type O individuals are often called "universal donors" since their blood can be transfused into persons with any blood type. Those with type AB blood are called "universal recipients" because they can receive blood of any type.<br><br>
							However, since approximately twice as many people in the general population have O and A blood types, a blood bank's need for this type of blood increases exponentially.<br><br>
							DO donate blood, only if you satisfy all of the following conditions<br>
							-	You are between age group of 18-60 years.<br>
							-	Your weight is 45 kgs or more.<br>
							-	Your hemoglobin is 12.5 gm% minimum.<br>
							-	Your last blood donation was 3 or more months earlier.<br>
							-	You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past.<br><br>
							DO NOT donate blood, if you have any of the following conditions<br>
							-	Cold / fever in the past 1 week.<br>
							-	Under treatment with antibiotics or any other medication.<br>
							-	Cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer, chronic kidney or liver disease, bleeding tendencies, venereal disease etc.<br><br>
							-	Major surgery in the last 6 months.<br>
							-	Vaccination in the last 24 hours.<br>
							-	Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.<br>
							-	Had fainting attacks during last donation.<br>
							-	Have regularly received treatment with blood products.<br>
							-	Shared a needle to inject drugs/ have history of drug addiction.<br>
							-	Had sexual relations with different partners or with a high risk individual.<br>
							-	Been tested positive for antibodies to HIV.<br><br>
							A Healthy Donor<br>
							A healthy diet helps ensure a successful blood donation, and also makes you feel better! Check out the following recommended foods to eat prior to your donation.<br><br>
								Low fat foods<br>
								Iron rich foods<br><br>							
								Blood Bank<br>
							A blood bank is a place designed especially for the storage of blood and blood products. Large coolers hold these products at a constant temperature and they are available at a moment's notice.<br><br>
							About Blood Donation<br>
							Donating blood is a life saving measure especially when you have a rare blood type. Blood donation is safe and simple. It takes only about 10 minutes to donate blood - less than the time of an average telephone call. We can save upto 3 to 4 precious lives by donating blood.<br><br>
